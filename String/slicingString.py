#slicing string
b = "Hello, World!"
print(b[2:5])

#slice from starting
b = "Hello, World!"
print(b[:5])

#slice to the end
b = "Hello, World!"
print(b[2:])


#negative indexing
b = "Hello, World!"
print(b[-5:-2])

# Prints string in reverse
b = "Hello, World!"
print(b[::-1])

# Extracting every second character "Hlo ol!"
b = "Hello, World!"
print(b[::2]) #Hlo ol



